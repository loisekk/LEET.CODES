class Solution(object):
    def minimumSum(self, num):
        """
        :type num: int
        :rtype: int
        """
        num1 = num
        num2 = (num , '|')
        while True :
            if num == (num1) and num ==(num2):
               [num1] < [num2] or [num2] < [num1]
            
        return num1 , num2

